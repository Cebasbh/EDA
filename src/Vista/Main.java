    private void EnterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterButtonActionPerformed
        String DNI = DNIField1.getText();
        String Name = NameField1.getText();
        String LastName = LastNameField1.getText();
        String Email = EmailField1.getText();
        String Phone = PhoneField1.getText();

        if (DNI.equals("") || Name.equals("") || LastName.equals("") || Email.equals("") || Phone.equals("")) {
            avisar("Datos Incompletos", "Aviso");
        }
        else if ("-".equals(TypeCombo1.getSelectedItem().toString())){
            avisar("Datos Incompletos", "Aviso");
        }
        else if ("-".equals(TopicCombo1.getSelectedItem().toString())){
            avisar("Datos Incompletos", "Aviso");
        }
        else if ("-".equals(PriorityCombo1.getSelectedItem().toString())){
            avisar("Datos Incompletos", "Aviso");
        }
        /*Regex*/
        else if (!isValidDNI(DNI)){
            avisar("DNI no Válido", "Aviso");
        }
        else if (!isValidEmail(Email)){
            avisar("Email no Válido", "Aviso");
        }
        else if (!isValidPhone(Phone)){
            avisar("Teléfono no Válido", "Aviso");
        }
        else {
            boolean opcion = confirmar("¿Estás seguro que deseas registrar este expediente?","Confirmación");
            if (opcion) {
                Documento doc = new Documento(DNI, Name, LastName, Email, Phone);
                String Priority = PriorityCombo1.getSelectedItem().toString();
                String Topic = TopicCombo1.getSelectedItem().toString();
                String Type = TypeCombo1.getSelectedItem().toString();
                Expediente exp = new Expediente(Priority, Topic, Type, doc);
                if (Priority.equals("2")){
                    ColaSinProcesar.EnqueuePrio2(exp);
                    ColaMaster.EnqueuePrio2(exp);
                }
                else if (Priority.equals("1")){
                    ColaSinProcesar.EnqueuePrio1(exp);
                    ColaMaster.EnqueuePrio1(exp);
                }
                DNIField1.setText("");
                NameField1.setText("");
                LastNameField1.setText("");
                EmailField1.setText("");
                PhoneField1.setText("");
                TypeCombo1.setSelectedItem("-");
                TopicCombo1.setSelectedItem("-");
                PriorityCombo1.setSelectedItem("-");
                notificar("Ingreso de expediente realizado exitosamente", "Notificación");
                pendientes();
                ListaAvisos.next();
            } else {
                notificar("Ingreso de expediente cancelado exitosamente", "Notificación");
            }
        }
    }//GEN-LAST:event_EnterButtonActionPerformed

