    private void GetnStartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GetnStartButtonActionPerformed
        if (DNIField2.getText().equals("")){
            Nodo primero = ColaSinProcesar.head;
                if (primero != null){
                    DNIField2.setText(primero.getExp().getDocumento().getDNI());
                    NameField2.setText(primero.getExp().getDocumento().getNombres());
                    LastNameField2.setText(primero.getExp().getDocumento().getApellidos());
                    EmailField2.setText(primero.getExp().getDocumento().getEmail());
                    TopicField2.setText(primero.getExp().getAsunto());
                    PhoneField2.setText(primero.getExp().getDocumento().getPhone());
                    PriorityField2.setText(primero.getExp().getPrioridad());
                    TypeField2.setText(primero.getExp().getTipo());
                    StateField2.setText(primero.getExp().getEstado());
                    }
                else{
                    avisar("No existen trámites nuevos", "Aviso");
                }
        }
        else{
            if ("-".equals(DepCombo2.getSelectedItem().toString())){
                avisar("Por favor, elige una dependencia válida", "Aviso");
            }
            else{
                boolean opcion = confirmar("¿Estás seguro que deseas iniciar el trámite de este expediente?","Confirmación");
                if (opcion){
                    Nodo iterador = ColaMaster.head;
                    Expediente Elemento = ColaSinProcesar.Dequeue();
                    Elemento.setDependencia(DepCombo2.getSelectedItem().toString());
                    Elemento.setEstado("En Proceso");
                    Elemento.setFechaingreso(Expediente.CalcularFecha());
                    if("1".equals(Elemento.getPrioridad())){
                        ColaEnProceso.EnqueuePrio1(Elemento);
                    }
                    else{
                        ColaEnProceso.EnqueuePrio2(Elemento);
                    }
                    iterador.getExp().setDependencia(DepCombo2.getSelectedItem().toString());
                    iterador.getExp().setFechaingreso(Expediente.CalcularFecha());
                    DNIField2.setText("");
                    NameField2.setText("");
                    LastNameField2.setText("");
                    EmailField2.setText("");
                    TopicField2.setText("");
                    PhoneField2.setText("");
                    PriorityField2.setText("");
                    TypeField2.setText("");
                    StateField2.setText("");
                    DepCombo2.setSelectedItem("-");
                    notificar("Trámite iniciado exitosamente", "Notificación");
                    pendientes();
                    ListaAvisos.add(Elemento);
                    ListaAvisos.next();

                }
                else{
                    notificar("Inicio de trámite cancelado exitosamente", "Notificación");
                }
            }
        }
    }//GEN-LAST:event_GetnStartButtonActionPerformed

