    private void actualizarTabs() {
    for (int i = 0; i < MainFrame.getTabCount(); i++) {
        JPanel tabPanel = new JPanel(new BorderLayout());
        tabPanel.setPreferredSize(new Dimension(132, 40));
        tabPanel.setBackground(i == MainFrame.getSelectedIndex()
                ? new Color(255, 117, 24)
                : new Color(190, 190, 190));

        JLabel tabLabel = new JLabel(MainFrame.getTitleAt(i), JLabel.CENTER);
        tabLabel.setFont(MainFrame.getFont());
        tabPanel.add(tabLabel, BorderLayout.CENTER);

        MainFrame.setTabComponentAt(i, tabPanel);
    }
    }

