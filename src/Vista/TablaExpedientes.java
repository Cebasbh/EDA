    public void ConstruirTabla(Nodo node){
        DefaultTableModel model = (DefaultTableModel) TablaExpediente.getModel();
            model.addRow(new Object[]{
                node.getExp().getId(),
                node.getExp().getDocumento().getDNI(),
                node.getExp().getDocumento().getNombres(),
                node.getExp().getDocumento().getApellidos(),
                node.getExp().getDocumento().getEmail(),
                node.getExp().getDocumento().getPhone(),
                node.getExp().getTipo(),
                node.getExp().getAsunto(),
                node.getExp().getDependencia(),
                node.getExp().getPrioridad(),
                node.getExp().getEstado(),
                node.getExp().getFechaingreso(),
                node.getExp().getFechatermino(),
                node.getExp().getActa(),
            });
    }
